### Rubyの基本

①値を出力する

```ruby
puts "Hello World"
```

```ruby
Hello World
```

メモ：
-  用いている「puts」はRubyの命令の一つ
- 「putsより後の値を出力せよ」という命令
- 「puts "〜"」をRubyのコンソールに書くと「"〜"」の部分がコンソールに出力される
- 「puts」の箇所はコンソールには出力されない
- 「puts」より後をクォーテーションで囲まなければエラーになる



②数値と文字列

【数値】
```ruby
puts 2 + 3   →  5
puts  7 - 4   →  3
```

メモ： 
- 文字列と違って数値はクォーテーションで囲まない

【文字列】
```ruby
puts 5 + 2
puts "5 + 2" 
```
```
ruby
7
5 + 2
```

メモ：
- プログラミングの世界では数値と文字列は別物
- 5 + 2は7になる
- しかし、"5 + 2"は文字列と認識されるのでそのまま出力する


③掛け算・割り算・余りの計算

```ruby
puts 4 * 7
puts 9 / 3
puts 7 % 2
```

```ruby
28
3
1
```

メモ：
- *   → 掛け算
- /   →  割り算
- % →  余りを出す


④文字列の連結

```ruby
puts "にんじゃ + わんこ"
```

```ruby
にんじゃわんこ
```

メモ：
- 文字列同士で+記号を用いると文字列を連結することができる

```ruby
puts "4" + "6"
puts 4 + 6
```

```ruby
46
10
```

メモ：
- "4" + "6"は文字列になるが、4 + 6だと数値と見なされる

⑤変数
```ruby
name ＝ "Mike"
```

メモ：
- 変数とは、値を入れておく箱のようなもの
- 変数は、上記のように「変数数＝ 値」で定義をする
- 「＝」は等しいではなく、代入の意味

```ruby
name ＝ "Mike"
puts name
```

```ruby
Mike
```

メモ：
- "Mike"という値が変数nameに代入される
- 代入された変数nameを出力すると"Mike"という値が出力される

【文字列の入った変数】
```ruby
message ＝ "こんにちは、"
puts message + "にんじゃわんこさん"
name ＝ "ひつじ仙人さん"
puts message + name
```

```ruby
こんにちは、にんじゃわんこさん
こんにちは、ひつじ仙人さん
```

```ruby
number1 = 10
puts namber1 + 3
number2 = 5
puts namber1 + namber2
```

メモ：
- 変数を呼び出すと代入した値に置き換わる
- 文字列が入った変数は文字列と同じように扱える
- 変数同士の文字列の連結や変数同士も可能
- 変数と数値の計算や変数同士の計算も可能

【変数の値を変更】
```ruby
number = "3"
puts number
number = "5"
puts number
```

```ruby
3
5
```

メモ：
- 変数は一度代入した値を変更できる
- 再び値を代入することで上書きされる

【自分自身に代入する】
```ruby
number ＝ 4
puts number
number ＝ number + 2
puts number
```

```ruby
4
6
```

メモ：
- すでに定義している"number = 4"に2を出すことで実現可能

## まとめ
- 変数を使用すると変数の部分は代入された値に置き換わる
- 文字列の連結であれば、文字列の入った変数と文字列の連結も、変数同士の連結も可能
- 数値が入った変数であれば計算可能
- 代入した値を変更する際は代入した値を代入し直すことで上書きされる

## 気づき・反省
- クォーテーションをつけ忘れることがあるのでつけ忘れないようにする
- 変数を使って出力する時はクォーテーションで囲まない
- "="を代入やと思いがちなので先入観を捨てる